  <h2>What are Normal and Critical values?</h2>
  <p class="p1">Critical/Panic values are defined as values that are outside the normal range to
    a degree that may constitute an immediate health risk to the individual or require
    immediate action on the part of the ordering physician. It is the policy of the clinical
    laboratory to call the critical values listed to a licensed provider as soon as completed and
    verified in compliance with hospital policies approved by the Lucile Packard Children's Hospital
    and Stanford Health Care hospital boards of directors. Read back of the critical values by the
    person receiving them to the person making the notification call is required.</p>

